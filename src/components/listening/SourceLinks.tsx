export function SourceLinks({
  spotifyUrl,
  appleUrl,
  size = 14,
}: {
  spotifyUrl?: string | null;
  appleUrl?: string | null;
  size?: number;
}) {
  if (!spotifyUrl && !appleUrl) return null;
  return (
    <span className="flex flex-none items-center gap-1.5">
      {spotifyUrl && (
        <Link
          href={spotifyUrl}
          target="_blank"
          rel="noreferrer"
          aria-label="Open in Spotify"
          title="Open in Spotify"
          className="text-[rgb(30,215,96)] transition-opacity hover:opacity-80"
        >
          <SpotifyLogo size={size} />
        </Link>
      )}
      {appleUrl && (
        <Link
          href={appleUrl}
          target="_blank"
          rel="noreferrer"
          aria-label="Open in Apple Music"
          title="Open in Apple Music"
          className="text-[rgb(252,70,107)] transition-opacity hover:opacity-80"
        >
          <AppleMusicLogo size={size} />
        </Link>
      )}
    </span>
  );
}
